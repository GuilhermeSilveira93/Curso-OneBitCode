let naveEspacial = ['SpaceX', 'Milenium Falcon']
console.log(naveEspacial)

naveEspacial.push('Obzidian') /*Adiciona um Elemento no final*/
console.log(naveEspacial)

naveEspacial.pop() /*Apaga Ultimo Elemento*/
console.log(naveEspacial)

naveEspacial.shift() /*Apaga o Primeiro Elemento*/
console.log(naveEspacial)

naveEspacial.unshift('Fenix') /*Adiciona um novo elemento no começo*/
console.log(naveEspacial)

console.log(naveEspacial.length) /*APRESENTA O NÚMERO DE CARACTERES.*/